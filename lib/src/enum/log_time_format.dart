/// An enum representing different formats for log timestamps.
///
/// - [userFriendly]: Indicates a user-friendly date and time format.
/// - [timestamp]: Indicates a timestamp format with milliseconds since epoch.
enum LogTimeFormat {
  /// Indicates a user-friendly date and time format.
  userFriendly,

  /// Indicates a timestamp format with milliseconds since epoch.
  timestamp,
}
